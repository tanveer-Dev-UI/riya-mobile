function keepOnlyLogoBrands(catalog) {
  const source = catalog && typeof catalog === "object" ? catalog : {};
  const filtered = {};

  Object.entries(source).forEach(([category, items]) => {
    filtered[category] = (Array.isArray(items) ? items : []).filter((item) =>
      ALLOWED_LOGO_BRANDS.has(String(item?.brand || "").trim())
    );
  });

  return filtered;
}

